public class ArithmeticDemo {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + a / b);
        System.out.println("(double)a / b = " + (double)a / b);
        System.out.println("a % b = " + a % b);
    }
}
